#!/usr/bin/env node
/**
 * derive-vehicle-anchors.mjs — 从 skeleton.json + mounted clip FK 推导骑手关节常量
 *
 * 用法：
 *   node scripts/derive-vehicle-anchors.mjs           # 只打印推导报告
 *   node scripts/derive-vehicle-anchors.mjs --write   # 写入 assets/vehicle-anchors.js
 *   node scripts/derive-vehicle-anchors.mjs --check   # 验证 assets/vehicle-anchors.js 是否与 FK 一致
 *
 * 公式：world = (npc.x + jx * scale * direction,  npc.y + jy * scale)
 *
 * mobile(ebike) 说明：原始代码中 scale = n.scale * 1.2，该 ×1.2 因子已折入常量
 * （如 wR = 14.4 * 1.2 = 17.28），视觉效果不变。vehicle-anchors.js 沿用折入后的值。
 */

import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const ROOT    = dirname(dirname(fileURLToPath(import.meta.url)));
const readJson = p => JSON.parse(readFileSync(p, 'utf8'));

const skeleton = readJson(join(ROOT, 'assets', 'skeleton.json'));
const manifest = readJson(join(ROOT, 'assets', 'manifest.json'));
const dp       = skeleton.skeletons.human.defaultPose;

const fk = (kf, joint) => {
  const base  = dp[joint] ?? [0, 0];
  const delta = kf[joint] ?? [0, 0];
  return [base[0] + delta[0], base[1] + delta[1]];
};

// ── Build anchor object from FK data ─────────────────────────────────────────

function buildAnchors() {
  const result = {};

  // bike (16 frames — use frame-0 for structural constants; all-frame avg for crank)
  {
    const kfs   = readJson(join(ROOT, 'assets', manifest.clips['bike'].path)).keyframes;
    const kf0   = kfs[0];
    const hip   = fk(kf0, 'body');
    const rhand = fk(kf0, 'r_hand');  // forward hand (jx=65 > l_hand jx=50)

    let sumMid = [0, 0];
    for (const kf of kfs) {
      const lf = fk(kf, 'l_foot');
      const rf = fk(kf, 'r_foot');
      sumMid[0] += (lf[0] + rf[0]) / 2;
      sumMid[1] += (lf[1] + rf[1]) / 2;
    }
    const avgMid = [sumMid[0] / kfs.length, sumMid[1] / kfs.length];

    result.bike = {
      hip_jx: hip[0], hip_jy: hip[1],
      bar_jx: rhand[0], bar_jy: rhand[1],
      crank_jx: Math.round(avgMid[0]), crank_jy: Math.round(avgMid[1]),
      wR: 25,
      rw_x_coeff: hip[0] - 5,    // = -5
      fw_x_coeff: rhand[0] + 7,  // = 72
    };
  }

  // mobike (1 frame — all values from frame 0)
  {
    const kf0   = readJson(join(ROOT, 'assets', manifest.clips['mobike'].path)).keyframes[0];
    const hip   = fk(kf0, 'body');
    const lhand = fk(kf0, 'l_hand');
    const rhand = fk(kf0, 'r_hand');
    const lfoot = fk(kf0, 'l_foot');
    const rfoot = fk(kf0, 'r_foot');

    result.mobike = {
      hip_jx: hip[0], hip_jy: hip[1],
      handL_jx: lhand[0], handL_jy: lhand[1],
      handR_jx: rhand[0], handR_jy: rhand[1],
      footF_jx: rfoot[0], footF_jy: rfoot[1],  // r_foot = forward footpeg
      footR_jx: lfoot[0], footR_jy: lfoot[1],  // l_foot = rear footpeg
    };
  }

  // mobile/ebike — ×1.2 factor was a legacy scale baked into the original draw code.
  // These folded values reproduce the same visual without the explicit *1.2 at call sites.
  {
    const kf0   = readJson(join(ROOT, 'assets', manifest.clips['mobile'].path)).keyframes[0];
    const hip   = fk(kf0, 'body');
    const lhand = fk(kf0, 'l_hand');  // forward hand (jx=32 > r_hand jx=27)
    const lfoot = fk(kf0, 'l_foot');
    const rfoot = fk(kf0, 'r_foot');
    const midJY = (lfoot[1] + rfoot[1]) / 2;

    const S12 = 1.2;
    result.mobile = {
      hip_jx: hip[0], hip_jy: hip[1],
      bar_jx: lhand[0], bar_jy: lhand[1],
      wR:            14.4 * S12,
      rw_x_coeff:    Math.round(hip[0]   - 19.2 * S12),
      fw_x_coeff:    Math.round(lhand[0] +  4.8 * S12),
      plat_y_coeff:  Math.round(midJY    +  2.4 * S12),
      boxW:          14.4 * S12,
      boxH:          13.2 * S12,
      boxCx_x_coeff: Math.round(hip[0]   - 21.6 * S12),
    };
  }

  return result;
}

// ── File generation ───────────────────────────────────────────────────────────

const fmtNum = v => Number.isInteger(v) ? String(v) : parseFloat(v.toFixed(2)).toString();

function generateFileText(anchors) {
  const { bike: b, mobike: mo, mobile: ml } = anchors;
  return [
    '// Generated by scripts/derive-vehicle-anchors.mjs — do not edit manually.',
    '// Regenerate: node scripts/derive-vehicle-anchors.mjs --write',
    '// Verify:     node scripts/derive-vehicle-anchors.mjs --check',
    'export const vehicleAnchors = {',
    '  bike: {',
    `    hip_jx: ${b.hip_jx}, hip_jy: ${b.hip_jy},`,
    `    bar_jx: ${b.bar_jx}, bar_jy: ${b.bar_jy},`,
    `    crank_jx: ${b.crank_jx}, crank_jy: ${b.crank_jy},`,
    `    wR: ${fmtNum(b.wR)},`,
    `    rw_x_coeff: ${fmtNum(b.rw_x_coeff)},`,
    `    fw_x_coeff: ${fmtNum(b.fw_x_coeff)},`,
    '  },',
    '  mobike: {',
    `    hip_jx: ${mo.hip_jx}, hip_jy: ${mo.hip_jy},`,
    `    handL_jx: ${mo.handL_jx}, handL_jy: ${mo.handL_jy},`,
    `    handR_jx: ${mo.handR_jx}, handR_jy: ${mo.handR_jy},`,
    `    footF_jx: ${mo.footF_jx}, footF_jy: ${mo.footF_jy},`,
    `    footR_jx: ${mo.footR_jx}, footR_jy: ${mo.footR_jy},`,
    '  },',
    '  mobile: {',
    `    hip_jx: ${ml.hip_jx}, hip_jy: ${ml.hip_jy},`,
    `    bar_jx: ${ml.bar_jx}, bar_jy: ${ml.bar_jy},`,
    `    wR: ${fmtNum(ml.wR)},`,
    `    rw_x_coeff: ${fmtNum(ml.rw_x_coeff)},`,
    `    fw_x_coeff: ${fmtNum(ml.fw_x_coeff)},`,
    `    plat_y_coeff: ${fmtNum(ml.plat_y_coeff)},`,
    `    boxW: ${fmtNum(ml.boxW)},`,
    `    boxH: ${fmtNum(ml.boxH)},`,
    `    boxCx_x_coeff: ${fmtNum(ml.boxCx_x_coeff)},`,
    '  },',
    '};',
    '',
  ].join('\n');
}

// ── CLI dispatch ──────────────────────────────────────────────────────────────

const mode = process.argv[2];
const ANCHORS_PATH = join(ROOT, 'assets', 'vehicle-anchors.js');

if (mode === '--write') {
  const anchors = buildAnchors();
  const text    = generateFileText(anchors);
  writeFileSync(ANCHORS_PATH, text, 'utf8');
  console.log('Wrote assets/vehicle-anchors.js');
  process.exit(0);
}

if (mode === '--check') {
  const anchors   = buildAnchors();
  const generated = generateFileText(anchors);
  const committed = readFileSync(ANCHORS_PATH, 'utf8');
  if (generated !== committed) {
    process.stderr.write('FAIL: assets/vehicle-anchors.js is stale.\n');
    process.stderr.write('Run: node scripts/derive-vehicle-anchors.mjs --write\n');
    process.exit(1);
  }
  console.log('ok: assets/vehicle-anchors.js matches FK derivation');
  process.exit(0);
}

// ── Default: print derivation report ─────────────────────────────────────────

const CLIPS = ['bike', 'mobike', 'mobile'];

for (const clipId of CLIPS) {
  const entry = manifest.clips[clipId];
  const raw   = readJson(join(ROOT, 'assets', entry.path));
  const kfs   = raw.keyframes;

  console.log(`\n${'─'.repeat(60)}`);
  console.log(`clip: ${clipId}  (${kfs.length} frame${kfs.length > 1 ? 's' : ''})`);
  console.log('─'.repeat(60));

  const kf0 = kfs[0];
  const hip    = fk(kf0, 'body');
  const lhand  = fk(kf0, 'l_hand');
  const rhand  = fk(kf0, 'r_hand');
  const lfootF = fk(kf0, 'l_foot');
  const rfootF = fk(kf0, 'r_foot');
  const fwdHand = rhand[0] >= lhand[0] ? rhand : lhand;
  const fwdName = rhand[0] >= lhand[0] ? 'r_hand' : 'l_hand';

  console.log('Frame 0 FK (joint-space, scale=1, direction=+1):');
  console.log(`  hip        (body):   [${hip[0].toFixed(1)}, ${hip[1].toFixed(1)}]`);
  console.log(`  l_hand:              [${lhand[0].toFixed(1)}, ${lhand[1].toFixed(1)}]`);
  console.log(`  r_hand:              [${rhand[0].toFixed(1)}, ${rhand[1].toFixed(1)}]`);
  console.log(`  forward hand (${fwdName}): [${fwdHand[0].toFixed(1)}, ${fwdHand[1].toFixed(1)}]`);
  console.log(`  l_foot:              [${lfootF[0].toFixed(1)}, ${lfootF[1].toFixed(1)}]`);
  console.log(`  r_foot:              [${rfootF[0].toFixed(1)}, ${rfootF[1].toFixed(1)}]`);

  let sumHip = [0, 0], sumMid = [0, 0];
  for (const kf of kfs) {
    const h  = fk(kf, 'body');
    const lf = fk(kf, 'l_foot');
    const rf = fk(kf, 'r_foot');
    sumHip[0] += h[0]; sumHip[1] += h[1];
    sumMid[0] += (lf[0] + rf[0]) / 2;
    sumMid[1] += (lf[1] + rf[1]) / 2;
  }
  const n = kfs.length;
  const avgHip = [sumHip[0] / n, sumHip[1] / n];
  const avgMid = [sumMid[0] / n, sumMid[1] / n];

  if (n > 1) {
    console.log(`\nAll-frame averages:`);
    console.log(`  hip avg:         [${avgHip[0].toFixed(1)}, ${avgHip[1].toFixed(1)}]`);
    console.log(`  crank (ft-mid):  [${avgMid[0].toFixed(1)}, ${avgMid[1].toFixed(1)}]`);
  }

  if (clipId === 'bike') {
    const HIP_JX = hip[0], BAR_JX = fwdHand[0];
    const wR = 25;
    console.log(`\nBike structural constants:`);
    console.log(`  rear wheel x:   hip_jx - 5  = ${HIP_JX} - 5 = ${HIP_JX - 5}  → rwx = n.x + ${HIP_JX - 5}*s*d`);
    console.log(`  front wheel x:  bar_jx + 7  = ${BAR_JX} + 7 = ${BAR_JX + 7}  → fwx = n.x + ${BAR_JX + 7}*s*d`);
    console.log(`  wheel radius:   ${wR}  → wCy = n.y - ${wR}*s`);
    console.log(`  crank center:   [${avgMid[0].toFixed(1)}, ${avgMid[1].toFixed(1)}]  (avg all frames)`);
  }

  if (clipId === 'mobile') {
    const HIP_JX = hip[0], HIP_JY = hip[1];
    const BAR_JX = fwdHand[0], BAR_JY = fwdHand[1];
    const midJY = (lfootF[1] + rfootF[1]) / 2;
    const S12 = 1.2;
    const wR = 14.4 * S12, rwOff = 19.2 * S12, fwOff = 4.8 * S12, platOff = 2.4 * S12;
    const boxW = 14.4 * S12, boxH = 13.2 * S12, boxCxOff = 21.6 * S12;
    console.log(`\nEbike structural constants (s = n.scale, no *1.2 factor):`);
    console.log(`  hip:        jx=${HIP_JX}, jy=${HIP_JY}`);
    console.log(`  bar (${fwdName}): jx=${BAR_JX}, jy=${BAR_JY}`);
    console.log(`  foot midY:  jy=${midJY.toFixed(1)}`);
    console.log(`\n  After folding s*1.2 → n.scale (current visual, no change):`);
    console.log(`  wR   = ${wR.toFixed(2)} * n.scale`);
    console.log(`  rwx  = n.x + ${(HIP_JX - rwOff).toFixed(2)} * n.scale * d  (→ Math.round = ${Math.round(HIP_JX - rwOff)})`);
    console.log(`  fwx  = n.x + ${(BAR_JX + fwOff).toFixed(2)} * n.scale * d  (→ Math.round = ${Math.round(BAR_JX + fwOff)})`);
    console.log(`  platY = n.y + ${(midJY + platOff).toFixed(2)} * n.scale     (→ Math.round = ${Math.round(midJY + platOff)})`);
    console.log(`  boxW = ${boxW.toFixed(2)}, boxH = ${boxH.toFixed(2)}`);
    console.log(`  boxCx = n.x + ${(HIP_JX - boxCxOff).toFixed(2)} * n.scale * d  (→ Math.round = ${Math.round(HIP_JX - boxCxOff)})`);
  }
}

console.log('\n');
